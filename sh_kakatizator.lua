-- --------------------------------------------------
-- Какакатизатор. Упроститель сложных числел типа 12235236254 в строку вида 12.3ккк
-- --------------------------------------------------

-- Спасибо за помощь log1st с df.abcd.bz
local function simpleSum(num)
	local k = 0

	while num / 1000 > 1 do
		k   = k + 1
		num = num / 1000
	end

	return string.sub(num,1,3) .. string.rep("k",k)
end
